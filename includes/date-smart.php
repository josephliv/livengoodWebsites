<div class="date-smart-hero">
    <h4>How Big Is The Problem?</h4>
    <hr>
    <div class="custom-section">
        <div class="custom-basis">
        <ul>
            <li>Violent behavior typically begins between the ages of 12 and 18.</li>
            <li>
                One in 10 teen girls and one in 11 teen boys admits to having experienced physical violence in a
                dating relationship in the past year.
            </li>
            <li>
                One in three teens say they know someone who has been physically assaulted or hurt by a dating
                partner.
            </li>
            <li>
                One in five teens admits to being emotionally abused in the past year.
            </li>
            <li>
                Among 11- to 14-year-olds who have been in relationships, 62% of them know friends who have been
                verbally abused by a boyfriend or girlfriend.
            </li>
            <li>
                Girls and young women between the ages of 16 and 24 experience the highest rate of intimate
                partner violence — almost triple the national average.
            </li>
            <li>
                Among female victims of intimate partner violence, 94% of those age 16-19 and 70% of those age
                20-24 were victimized by a current or former boyfriend or girlfriend.
            </li>
            <li>
                The severity of intimate partner violence is often greater in cases where the pattern of abuse
                was established in adolescence.
            </li>
        </ul>
