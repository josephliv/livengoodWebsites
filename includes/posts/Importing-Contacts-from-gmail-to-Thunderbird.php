            <h3>Step by step instructions </h3>
            <hr>

            <ol style="text-align: left !important;">
                <li>From your Gmail account, click the contact icon on the right side and then look for the new tab icon
                    on the
                    top, click this. This will open the contact in a new tab.</li>
                <li>Now you want to select one of the contacts so that the select all will appear at the top, now select
                    all
                    from there.</li>
                <li>You will see 3 dots right next to an envelope icon, click this and click export.</li>
                <li>Select the “Outlook CSV” option and click Export.</li>
                <li>Save this file somewhere because we will need it in the next step.</li>
                <li>Open Thunderbird and click the “Address Book” button at the top, this will open a new window.</li>
                <li>Now click the tools &gt; import, select the Address Books option and click next.</li>
                <li>Click next again and it will ask you for the file you just downloaded, select it and click on Open.
                </li>
                <li>This box that opens will by default have everything selected but this will not work because Gmail
                    and
                    Thunderbird have different storage fields for each item. So you need to go through this and match
                    everything
                    that you want to be imported.</li>
                <li>To do this, select the item and use the “move up” or “move down” buttons on the right to move the
                    left side
                    to match the right side columns.</li>
                <li>To help with this, here’s the time stamp at around <a href="https://youtu.be/07yIR0spO3I?t=138"
                        target="_blank" rel="noopener nofollow noopener">2:18</a> in the video so you can see how this
                    works, it
                    can be confusing for the first time doing it. It was a little for me. The first couple of attempts
                    it wasn’t
                    displaying the contacts info correctly.</li>
            </ol>

